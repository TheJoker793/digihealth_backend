using Hangfire;
using Notification_microservice.Infrastructure.Extensions;
using Notification_microservice.Infrastructure.Jobs;
using Notification_microservice.Infrastructure.Persistence;
using Microsoft.EntityFrameworkCore;
using Microsoft.OpenApi.Models;
using Serilog;
using Notification_microservice.Middlewares;

var builder = WebApplication.CreateBuilder(args);

// ????????????????????????????????????????????????
// SERILOG
// ????????????????????????????????????????????????
Log.Logger = new LoggerConfiguration()
    .ReadFrom.Configuration(builder.Configuration)
    .Enrich.FromLogContext()
    .WriteTo.Console()
    .CreateLogger();

builder.Host.UseSerilog();

// ????????????????????????????????????????????????
// SERVICES
// ????????????????????????????????????????????????
var connStr = builder.Configuration.GetConnectionString("DefaultConnection")!;

builder.Services
    .AddDatabase(connStr)
    .AddRepositories()
    .AddApplicationServices()
    .AddSenders(builder.Configuration)
    .AddRedis(builder.Configuration)
    .AddRendering()
    .AddMessaging(builder.Configuration)
    .AddHangfireJobs(connStr)
    .AddJwtFromAuthSvc(builder.Configuration)
    .AddValidatorsAndMapping();

builder.Services.AddControllers();
builder.Services.AddEndpointsApiExplorer();

// ?? Swagger avec auth Bearer ?????????????????????
builder.Services.AddSwaggerGen(c =>
{
    c.SwaggerDoc("v1", new OpenApiInfo
    {
        Title = "Notification.svc",
        Version = "v1",
        Description = "Microservice de notifications multi-canal : Email, SMS, Push, InApp"
    });

    c.AddSecurityDefinition("Bearer", new OpenApiSecurityScheme
    {
        Name = "Authorization",
        Type = SecuritySchemeType.Http,
        Scheme = "bearer",
        BearerFormat = "JWT",
        In = ParameterLocation.Header,
        Description = "JWT Bearer token"
    });

    c.AddSecurityRequirement(new OpenApiSecurityRequirement
    {
        {
            new OpenApiSecurityScheme
            {
                Reference = new OpenApiReference
                    { Type = ReferenceType.SecurityScheme, Id = "Bearer" }
            },
            Array.Empty<string>()
        }
    });
});

// ?? Health checks ????????????????????????????????
builder.Services
    .AddHealthChecks()
    .AddSqlServer(connStr, name: "sqlserver")
    .AddRedis(
        builder.Configuration.GetConnectionString("Redis") ?? "localhost:6379",
        name: "redis")
    .AddRabbitMQ(
        $"amqp://{builder.Configuration["RabbitMq:Username"]}:{builder.Configuration["RabbitMq:Password"]}@{builder.Configuration["RabbitMq:Host"]}/",
        name: "rabbitmq");

// ????????????????????????????????????????????????
// BUILD
// ????????????????????????????????????????????????
var app = builder.Build();

// ?? Migrations auto au démarrage ?????????????????
using (var scope = app.Services.CreateScope())
{
    var db = scope.ServiceProvider.GetRequiredService<NotificationDbContext>();
    await db.Database.MigrateAsync();
}

// ????????????????????????????????????????????????
// PIPELINE MIDDLEWARES (ordre obligatoire)
// ????????????????????????????????????????????????

// 1. Exception handler — doit ętre en premier
app.UseMiddleware<ExceptionHandlingMiddleware>();

// 2. Swagger (dev uniquement)
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI(c =>
    {
        c.SwaggerEndpoint("/swagger/v1/swagger.json", "Notification.svc v1");
        c.RoutePrefix = "swagger";
    });
}

// 3. Logging des requętes HTTP
app.UseSerilogRequestLogging();

// 4. HTTPS
app.UseHttpsRedirection();

// 5. Auth
app.UseAuthentication();
app.UseAuthorization();

// 6. Hangfire Dashboard — accčs local uniquement
app.UseHangfireDashboard("/hangfire", new DashboardOptions
{
    Authorization = [new Hangfire.Dashboard.LocalRequestsOnlyAuthorizationFilter()]
});

// 7. Enregistrer les jobs récurrents
HangfireJobRegistrar.EnregistrerJobs();

// 8. Health checks
app.MapHealthChecks("/health");
app.MapHealthChecks("/health/ready", new Microsoft.AspNetCore.Diagnostics.HealthChecks.HealthCheckOptions
{
    Predicate = _ => true
});
app.MapHealthChecks("/health/live", new Microsoft.AspNetCore.Diagnostics.HealthChecks.HealthCheckOptions
{
    Predicate = _ => false   // live = juste "l'app tourne"
});

// 9. Controllers
app.MapControllers();

app.Run();
